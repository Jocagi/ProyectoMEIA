    private void cargarContenido()
    {
        //Imagen            
        try {
        BufferedImage img = ImageIO.read(new File(MaterialBuscado.material.PathImagen));
        Image dimg = img.getScaledInstance(Fotografia.getWidth(), Fotografia.getHeight(), Image.SCALE_SMOOTH);
        ImageIcon imageIcon = new ImageIcon(dimg);
        Fotografia.setIcon(imageIcon);
          
        } catch (IOException ex) {
            Logger.getLogger(MenuAplicacion.class.getName()).log(Level.SEVERE, null, ex);
        }
        
        //Nombre
        Nombre.setText(MaterialBuscado.material.Nombre);
        Tipo.setText(MaterialBuscado.material.Tipo);
        Tiempo.setText(MaterialBuscado.material.TiempoDegradacion);    
    }
    
